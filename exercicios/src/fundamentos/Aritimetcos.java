package fundamentos;

public class Aritimetcos {
	public static void main(String[] args) {
		  System.out.println(2+3);
		  var X = 34.56;
		  double Y = 2.2;
		  System.out.println(X + Y);
		  System.out.println(X - Y);
		  System.out.println(X * Y);
		  System.out.println(X / Y);
		  int a = 8;
		  int b =3;
		  System.out.println(a + b);
		  System.out.println(a - b);
		  System.out.println(a * b);
		  System.out.println(a / b);
		  System.out.println(a / (double)b); // para numeros flutuantes , poder ser double ou pode ser float - double tem mais precisao .melhor double
		  System.out.println(8 % 3);
		  System.out.println(a % b);
		  System.out.println(X+Y - a*b);
	}

}
